	<div class="helper-content hidden" id="helper-resumen-mensual">
	  <b>Resumen Mensual:</b> Los principales datos sobre el IVA de su negocio, según el mes elegido en el filtro superior. 
	</div>
	
	<div class="helper-content hidden" id="helper-iva-por-pagar">
	  <b>IVA por pagar:</b> El IVA por pagar mensual es el dato del cálculo automático entre sus débitos y créditos fiscales; es decir entre el IVA que ha facturado a sus clientes y el IVA que ya ha pagado a proveedores. Eso sí, considerando sus derechos de acreditación.
	</div>
	
	<div class="helper-content hidden" id="helper-iva-emitidas">
	  <b>IVA de facturas emitidas:</b> El total de IVA que le ha facturado a sus clientes en el mes respectivo. Se incluye en esta suma las diferentes tasas de IVA.
	</div>
	
	<div class="helper-content hidden" id="helper-iva-recibidas">
	  <b>IVA de facturas recibidas:</b> El total de IVA que le han facturado sus proveedores. Se incluye en esta suma las diferentes tasas de IVA.
	</div>

	<div class="helper-content hidden" id="helper-proporcion">
		
		<b>Proporción anual de ventas por tipo de IVA:</b> El IVA en Costa Rica contempla diferentes tasas del impuesto. Este gráfico muestra los diferentes porcentajes según tasa de IVA por los que ha vendido su negocio a lo largo del año fiscal.
		</br>
	  <b>Exentas sin derecho a crédito:</b> Son las ventas realizadas a sujetos exentos que afectan los derechos de acreditación. Es decir, afectan la posibilidad de descontar parte del IVA cancelado a proveedores en cada liquidación mensual. 
		</br>
		<b>Exentas con derecho a crédito:</b> Son las ventas realizadas a sujetos exentos pero que no afectan los derechos de acreditación. Es decir, estas ventas no afectan la posibilidad de descontar parte del IVA cancelado a proveedores en cada liquidación mensual. 
		
	</div>
	
	<div class="helper-content hidden" id="helper-prorrata-operativa">
		<b>La prorrata operativa</b> es el cálculo de la proporcionalidad de las ventas con base en el que se definen los derechos de acreditación para el año actual. Este cálculo está compuesto por los diferentes tipos de IVA en las ventas del período anterior.
	</div>
	
	<div class="helper-content hidden" id="helper-prorrata-estimada">
		<b>La prorrata estimada</b> es el cálculo en tiempo real de la proporcionalidad de las ventas realizadas hasta el momento del año. El cálculo contempla las ventas según tipo de IVA e indica: 1) Los derechos de acreditación reales en el año en curso y 2) los derechos de acreditación operativos para el siguiente período fiscal. 
	</div>
	
	<div class="helper-content hidden" id="helper-liquidacion-estimada">
		<b>La liquidación estimada del IVA</b> es la diferencia entre el IVA pagado cada mes con base en la prorrata operativa vigente y el IVA que realmente se debería pagar con base en la prorrata estimada. Esta diferencia da el monto estimado por pagar al momento de liquidar el impuesto a final del año fiscal. El monto puede ser positivo o 
		negativo, ya sea que usted deba cancelar más al Ministerio de Hacienda o por, el contrario, cuente con un crédito a favor en sus impuestos.
	</div>
